#!/bin/bash
# session-workflow.sh
# セッション管理のワークフローを体験するデモスクリプト
#
# Claude Code のセッション機能を使い分ける実践例:
#   - 名前付きセッションの作成・再開
#   - タスク切替時のセッション管理
#   - 非対話モードとの組み合わせ

set -euo pipefail

echo "=== Claude Code セッション管理ワークフロー ==="
echo ""
echo "このスクリプトは実行するものではなく、コマンド例の参考資料です。"
echo "各コマンドを手動でターミナルに入力して試してください。"
echo ""

cat << 'WORKFLOW'
# ==========================================
# ワークフロー 1: 機能開発セッション
# ==========================================

# 1. 名前付きセッションで開発開始
claude -n "feature-user-auth"

# 2. セッション内で作業（認証機能の実装）
#    ... コーディング作業 ...

# 3. 中断する場合 → Ctrl+C で抜ける（セッションは自動保存）

# 4. 後で再開
claude -r "feature-user-auth"

# 5. 機能完了後、セッション一覧を確認
claude -r    # 引数なしでセッション一覧ピッカーが開く

# ==========================================
# ワークフロー 2: マルチタスク切替
# ==========================================

# タスク A: バグ修正
claude -n "bugfix-login-error"
# → /clear でコンテキストをリセットせず、セッション自体を切り替える

# タスク B に切替: コードレビュー（別セッション）
# Ctrl+C で抜けて:
claude -n "review-pr-123"

# タスク A に戻る（コンテキスト維持）
claude -r "bugfix-login-error"

# ==========================================
# ワークフロー 3: 非対話 → 対話の連携
# ==========================================

# 1. 非対話でコード生成（CI/スクリプトから）
claude -p "app.js の calculateDiscount 関数のバグを修正して" --max-turns 3

# 2. 結果を確認して、対話セッションで深掘り
claude -c  # 直前のセッションを継続

# ==========================================
# ワークフロー 4: JSON出力でスクリプト連携
# ==========================================

# レビュー結果を JSON で取得してパース
RESULT=$(claude -p "app.js のコードレビューをして。JSON形式で severity と message を返して" \
  --output-format json \
  --max-turns 1)

# jq でパース
echo "$RESULT" | jq -r '.result' 2>/dev/null

# パイプで入力
cat app.js | claude -p "このコードの改善点を3つ挙げて" --max-turns 1

WORKFLOW
